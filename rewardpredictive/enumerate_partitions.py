def enumerate_n_partitions(num_states, num_partitions):
    if num_partitions == 1:
      return np.zeros([1, num_states], dtype=np.int8)

    partition_idx_list = []
    for part_list in _enumerate_partitions(np.arange(num_states), num_partitions):
        partition_idx = np.zeros(num_states, dtype=np.uint8)
        for i, part in enumerate(part_list):
            for s_idx in part:
                partition_idx[s_idx] = i
        partition_idx_list.append(partition_idx)
    return np.array(partition_idx_list).astype(np.int8)


def enumerate_all_partitions(num_states):
    part_list = [enumerate_n_partitions(num_states, n) for n in range(1, num_states + 1)]
    part_list = np.concatenate(part_list, axis=0)
    return part_list
